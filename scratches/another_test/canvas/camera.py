class Camera:

    def __init__(self, canvas: "_canvas.Canvas"):
        self.canvas = canvas
        self.context = canvas.context

        self._is_dirty = True
        self.projection = None
        self.modelview = None
        self.viewport = None
        self.clip = None
        self.up = None
        self.right = None
        self.forward = None

        self.up_norm = None
        self.right_norm = None
        self.forward_norm = None

        self.position = _point.Point(_decimal(0.0),
                                     _decimal(Config.settings.eye_height),
                                     _decimal(0.0))

        self.eye = _point.Point(_decimal(0.0),
                                _decimal(Config.settings.eye_height + 0.5),
                                _decimal(75.0))

        self.angle = _angle.Angle.from_points(self.position, self.eye)

        self.angle_from_center = _angle.Angle.from_points(
            ZERO_POINT.copy(), self.eye)

        self.focal_distance = _line.Line(self.eye, self.position).length()

        self.distance_from_center = _line.Line(ZERO_POINT, self.eye).length()

        self._calculate_camera()

        self.position.bind(self._update_camera)
        self.eye.bind(self._update_camera)

    def reset(self):
        with self.position and self.eye:
            self.position.x = _decimal(0.0)
            self.position.y = _decimal(Config.settings.eye_height)
            self.position.z = _decimal(0.0)

            self.eye.x = _decimal(0.0)
            self.eye.y = _decimal(Config.settings.eye_height + 0.5)
            self.eye.z = _decimal(75.0)

        self._update_camera(None)

    def _update_camera(self, _=None):
        if self.context.is_locked:
            self._is_dirty = True
        else:
            wx.CallAfter(self.canvas.Refresh, False)

    def get_objects_in_view(self, objs: list) -> list:
        if self.clip is None:
            self._is_dirty = True

        if self._is_dirty:
            self._update_views()

        obj_len = len(objs)

        clip = self.clip
        eye = self.eye
        aabb_intersects_frustum = self.aabb_intersects_frustum

        res = [[_line.Line(eye, obj.position).length(), obj]
               for obj in objs
               if aabb_intersects_frustum(obj.hit_test_rect, clip)]

        # sort the objects by distance from the camera
        res = sorted(res, key=lambda o: o[0])

        print('num objects culled:', obj_len - len(res))

        # we need to have the order as far -> near, we also trim off the distance
        # that was used for saorting
        return [res[i][1] for i in range(len(res) - 1, -1, -1)]

    @staticmethod
    def aabb_intersects_frustum(ht_rects: list, view_proj: np.ndarray) -> bool:
        """
        Return True if ANY AABB in ht_rects intersects the view frustum defined
        by view_proj (projection @ view).

        ht_rects: iterable of AABBs, each AABB being (min_point, max_point)
                  where min_point/max_point have .x/.y/.z

        view_proj: 4x4 numpy array (projection @ view), dtype float32/float64
        """
        # ensure matrix dtype for corners creation
        dtype = view_proj.dtype

        for ht_rect in ht_rects:
            # unpack min/max points
            mn = ht_rect[0]
            mx = ht_rect[1]

            corners = np.array(
                [
                    [mn.x, mn.y, mn.z, 1.0],
                    [mn.x, mn.y, mx.z, 1.0],
                    [mn.x, mx.y, mn.z, 1.0],
                    [mn.x, mx.y, mx.z, 1.0],
                    [mx.x, mn.y, mn.z, 1.0],
                    [mx.x, mn.y, mx.z, 1.0],
                    [mx.x, mx.y, mn.z, 1.0],
                    [mx.x, mx.y, mx.z, 1.0],
                ], dtype=dtype
            )  # (8,4)

            clip = corners @ view_proj.T  # (8,4)  (row-vector convention)

            # clip coords: [x, y, z, w]
            x = clip[:, 0]
            y = clip[:, 1]
            z = clip[:, 2]
            w = clip[:, 3]

            # outside tests: if all corners are outside any of the six clip planes, the box is outside
            # left:   x < -w  for all corners  -> outside
            # right:  x >  w  for all corners  -> outside
            # bottom: y < -w
            # top:    y >  w
            # near:   z < -w
            # far:    z >  w
            if (
                np.all(x < -w) or np.all(x > w) or
                np.all(y < -w) or np.all(y > w) or
                np.all(z < -w) or np.all(z > w)
            ):
                # this AABB is completely outside; check next AABB
                continue

            # if we get here, this AABB is at least partially inside -> we can return True
            return True

        # none of the AABBs intersect
        return False

    def set(self):
        self._calculate_camera()
        self._update_views()
        camera = self.eye.as_float + self.position.as_float + tuple(self.up.tolist())

        GLU.gluLookAt(*camera)

    def _calculate_camera(self):
        eye = self.eye.as_numpy
        pos = self.position.as_numpy

        forward = pos - eye

        fn = np.linalg.norm(forward)
        if fn < 1e-6:
            return

        forward = forward / fn

        gf = np.linalg.norm(forward)
        if gf < 1e-6:
            forward_ground = np.array([0.0, 0.0, -1.0],
                                      dtype=np.dtypes.Float64DType)
        else:
            forward_ground = forward / gf

        self.forward_norm = gf

        world_up = np.array([0.0, 1.0, 0.0],
                            dtype=np.dtypes.Float64DType)

        right = np.cross(world_up, forward_ground)  # NOQA

        rn = np.linalg.norm(right)
        if rn < 1e-6:
            right = np.array([1.0, 0.0, 0.0],
                             dtype=np.dtypes.Float64DType)
        else:
            right = right / rn

        self.right_norm = rn

        up = np.cross(forward_ground, right)  # NOQA

        un = np.linalg.norm(up)
        if un < 1e-6:
            up = np.array(
                [0.0, 1.0, 0.0],
                dtype=np.dtypes.Float64DType
            )
        else:
            up = up / un

        self.up_norm = un

        self.up = up
        self.right = right
        self.forward = forward_ground

        self.focal_distance = _line.Line(self.eye, self.position).length()

    def _update_views(self):
        if not self._is_dirty:
            return

        self._calculate_camera()
        with self.context:
            self._is_dirty = False
            self.projection = np.array(GL.glGetDoublev(GL.GL_PROJECTION_MATRIX)).reshape((4, 4))
            self.modelview = np.array(GL.glGetDoublev(GL.GL_MODELVIEW_MATRIX)).reshape((4, 4))
            self.viewport = GL.glGetIntegerv(GL.GL_VIEWPORT)
            self.clip = (self.projection @ self.modelview).astype(np.float32)

    def rotate(self, dx, dy):
        """
        Moves the camera position keeping the focused point locked.
        """
        self._is_dirty = True
        eye = self._rotate_about(dx, dy, self.eye, self.position)
        self.eye += eye - self.eye

    @staticmethod
    def _rotate_about(dx: int, dy: int, p1: _point.Point, p2: _point.Point) -> np.ndarray:
        """
        Moves the camera position keeping the focused point locked.
        """
        # This is a constant that can be adjusted. This is a hard limit
        # to prevent gimbal lock from occuring when looking straight up or
        # straight down. You can set it to a smaller number but do not increase
        # it past 89.9 otherwise gimbal lock can occur.
        p1 = p1.as_numpy
        p2 = p2.as_numpy

        max_pitch = 89.9

        offset = p1 - p2
        dist = np.linalg.norm(offset)

        if dist < 1e-6:
            return np.array([0.0, 0.0, 0.0], dtype=np.float64)

        up = np.array([0.0, 1.0, 0.0], dtype=np.float64)

        def _rodrigues(v, k, angle_rad):
            k = k / np.linalg.norm(k)
            cos_a = math.cos(angle_rad)
            sin_a = math.sin(angle_rad)  # NOQA
            return ((v * cos_a) + (np.cross(k, v) * sin_a) +  # NOQA
                    (k * (np.dot(k, v)) * (1.0 - cos_a)))

        yaw_offset = _rodrigues(offset, up, math.radians(dx))
        yaw_offset_n = np.linalg.norm(yaw_offset)

        if yaw_offset_n < 1e-6:
            return np.array([0.0, 0.0, 0.0], dtype=np.float64)

        yaw_dir = yaw_offset / yaw_offset_n

        horiz_len = math.hypot(yaw_dir[0], yaw_dir[2])

        cur_pitch_deg = _decimal(
            math.degrees(math.atan2(yaw_dir[1], horiz_len)))

        desired_pitch = cur_pitch_deg + dy
        if desired_pitch > max_pitch or desired_pitch < -max_pitch:
            # block pitch movement entirely (yaw still applies)
            dy = 0.0

        if abs(dy) < 1e-6:
            final_offset = yaw_dir * dist
        else:
            right = np.cross(yaw_dir, up)  # NOQA
            rn = np.linalg.norm(right)
            if rn < 1e-6:
                final_offset = yaw_dir * dist
            else:
                right = right / rn
                rotated = _rodrigues(yaw_offset, right, math.radians(dy))

                rnorm = np.linalg.norm(rotated)
                if rnorm < 1e-6:
                    final_offset = yaw_dir * dist
                else:
                    # explicitly restore original distance to avoid shrink/grow
                    final_offset = rotated * (dist / rnorm)

        new_point = p2 + final_offset

        return _point.Point(_decimal(new_point[0]), _decimal(new_point[1]),
                            _decimal(new_point[2]))

    def pan_tilt(self, dx, dy):
        """
        Moves the camera position keeping the focused point locked.

        Pan and Tilt camera movements.
        """
        self._is_dirty = True
        position = self._rotate_about(dx, dy, self.position, self.eye)
        self.position += position - self.position

    def zoom(self, delta, *_):
        """
        This has a similiar movement appearance as Dolly except there are hard
        limits as to how far it is able to move where as Dolly does not.
        This also doesn't change the camera position at all. It simply shinks
        or expands the distance between the focal point and the camera position.
        """
        move = self.forward * float(delta)
        print('delta:', delta)
        # If moving would invert eye and pos, prevent crossing pos
        if delta > 0 and self.focal_distance <= 0.1:
            return
        elif delta < 0 and self.focal_distance >= 150.0:
            return

        self._is_dirty = True
        self.eye += move

    def walk(self, dx, dy, speed):
        """
        This movement is a bit tricky to explain in terms of camera movement.
        If you think about what you do as a person when you walk this will
        mimick as close as I could get to that movement. so basically if you
        want to walk straight forward and back you get that from this function.
        That is the same as a Dolly camera movement.

        if you go left and right the movement you get is as if you are turning
        on the ball of your foot. This is comparable to the "Pan" movement of a
        camera.

        If you hold left or right and press up or down at the same time you get
        a combination of the 2 above movements. kind of like what it would be
        like when you walk in an arc to either left or right.
        """

        # Build desired move from input
        input_mag = math.sqrt((dx * dx) + (dy * dy))
        if input_mag == 0:
            return

        move_dir = self.right * float(dx) + self.forward * float(dy)

        mdn = np.linalg.norm(move_dir)
        if mdn < 1e-6:
            return

        move_dir = move_dir / mdn
        move = move_dir * (input_mag * speed)

        self._is_dirty = True
        with self.eye and self.position:
            self.eye += move
            self.position += move

    def truck_pedistal(self, dx, dy, speed):
        """
        this is as the function name states. It is a Truck (left right)
        and a Pedistal (up down) movement
        """

        # Build desired move from input
        input_mag = math.sqrt((dx * dx) + (dy * dy))
        if input_mag == 0:
            return

        move_dir = self.right * float(dx) + self.up * float(dy)

        mdn = np.linalg.norm(move_dir)
        if mdn < 1e-6:
            return

        move_dir = move_dir / mdn

        move = move_dir * (input_mag * speed)

        with self.eye:
            self.eye += move

        self._is_dirty = True
        self.position += move

    def project_point(self, point: _point.Point) -> _point.Point:
        """
        Project a world-space _point.Point to window coordinates (top-left origin).
        Returns (winx, winy_top, winz) where winz is in [0,1].
        """

        with self.context:
            winx, winy, winz = GLU.gluProject(float(point.x), float(point.y), float(point.z),
                                              self.modelview, self.projection, self.viewport)

        # convert to top-left origin to match wx mouse coordinates
        winy_top = _decimal(self.viewport[3]) - _decimal(winy)
        return _point.Point(_decimal(winx), winy_top, _decimal(winz))

    def unproject_point(self, point: _point.Point) -> _point.Point:
        """
        Unproject window coordinates (top-left origin) and
        winz back to a world _point.Point.
        """

        with self.context:
            # convert top-left y back to OpenGL bottom-left y
            winy = _decimal(self.viewport[3]) - point.y

            x, y, z = GLU.gluUnProject(float(point.x), float(winy), float(point.z),
                                       self.modelview, self.projection, self.viewport)

        return _point.Point(_decimal(x), _decimal(y), _decimal(z))
