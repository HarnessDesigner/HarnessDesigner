    @property
    def aspect_ratio(self) -> float:
        """
        Return the aspect ratio.

        :rtype: float
        """
        if self._viewport is None or self._viewport[3] == 0:
            return 1.0

        return float(self._viewport[2]) / float(self._viewport[3])

    def Reset(self):
        """
        Reset the camers back to its home position.
        """
        with self._position and self._focal_position:
            self._focal_position.x = 0.0
            self._focal_position.y = self.canvas.config.floor.ground_height + 10.0
            self._focal_position.z = 0.0

            self._position.x = 0.0
            self._position.y = self.canvas.config.floor.ground_height + 100.0

            if self.canvas.config.edit2d.enable:
                # Keep the reset pose inside the locked straight-down view
                # instead of jumping to the perspective default.
                self._position.z = 0.0
            else:
                self._position.z = 75.0

        self._update_camera(None)
        self._send_event(_events.EVT_GL_CAMERA_RESET)

    def SetTopDownLock(self, enable: bool):
        """
        Enable/disable the locked straight-down ("bird's-eye") view.

        Writes ``self.canvas.config.edit2d.enable`` — the single source of
        truth read everywhere else (``_calculate_camera``, ``Rotate``,
        ``PanTilt``, ``Reset``, the mouse/key dispatch layers, and the
        rotation-ring gizmo).

        :param enable: Enable or disable the lock.
        :type enable: bool
        """
        enable = bool(enable)

        if enable:
            with self._position:
                offset = self._focal_position.as_numpy - self._position.as_numpy
                focal_distance = float(np.linalg.norm(offset))
                if focal_distance < 1e-6:
                    focal_distance = 1.0

                self._position.x = self._focal_position.x
                self._position.y = self._focal_position.y + focal_distance
                self._position.z = self._focal_position.z

        self.canvas.config.edit2d.enable = enable

        self._is_dirty = True
        self._update_camera(None)

    def _update_camera(self, _=None):
        """
        Update the camera.

        :type _: None
        """
        self._camera_moved_since_last_cull = True

        if (
            self.canvas.config.floor.enable_floor_lock and
            self._position.y < self.canvas.config.floor.ground_height + 0.05
        ):
            with self._position:
                self._position.y = self.canvas.config.floor.ground_height + 0.25

        self._is_dirty = True

        _app.CallAfter(self.canvas.update)

    @property
    def has_camera_moved(self) -> bool:
        """
        Has the camera moved.

        :returns: If the camera has moved
        :rtype: bool
        """
        res = self._camera_moved_since_last_cull
        self._camera_moved_since_last_cull = False
        return res

    @staticmethod
    def _extract_frustum_planes(view_proj: np.ndarray) -> np.ndarray:
        """
        Returns planes as an array shape (6,4): [A,B,C,D] per plane.

        Planes are normalized (A,B,C length = 1).

        Order: left, right, bottom, top, near, far

        :type view_proj: :class:`np.ndarray`

        :rtype: :class:`np.ndarray`
        """

        m = np.asarray(view_proj, dtype=np.float32)

        # rows
        r0 = m[0, :]
        r1 = m[1, :]
        r2 = m[2, :]
        r3 = m[3, :]

        planes = np.stack([(r3 + r0),  # left
                          (r3 - r0),  # right
                          (r3 + r1),  # bottom
                          (r3 - r1),  # top
                          (r3 + r2),  # near  (OpenGL style)
                          (r3 - r2),  # far
                           ], axis=0)

        # normalize planes
        n = np.linalg.norm(planes[:, 0:3], axis=1)
        planes = planes / n[:, None]

        return planes

    def Set(self):
        """
        Set the camera positon.
        """

        self._calculate_camera()

        camera = (self._position.as_float +
                  self._focal_position.as_float +
                  tuple(float(str(v)) for v in self._up.tolist()))

        GLU.gluLookAt(*camera)
        self._update_views()

    @_debug.logfunc
    def _calculate_camera(self):
        """
        Calculate the camera.
        """

        pos = self._position.as_numpy
        fp = self._focal_position.as_numpy

        forward = fp - pos

        focal_distance = np.linalg.norm(forward)
        if focal_distance < 1e-6:
            return

        if self.canvas.config.edit2d.enable:
            self._forward = _LOCKED_FORWARD.copy()
            self._right = _LOCKED_RIGHT.copy()
            self._up = _LOCKED_UP.copy()
            self._focal_distance = float(focal_distance)
            return

        forward = forward / focal_distance

        world_up = _WORLD_UP.copy()
        dot = float(np.dot(forward, world_up))

        # When the view direction gets too close to the world-up vector the
        # look-at basis becomes ill-conditioned. Preserve the previous camera up
        # vector in that case so zooming through very small focal distances does
        # not cause a sudden 90° roll.
        if abs(dot) > 0.9999 and self._up is not None:
            up = np.array(self._up, dtype=np.float32)
            un = np.linalg.norm(up)
            if un < 1e-6:
                up = world_up.copy()
                # un = 1.0
            else:
                up = up / un

            right = np.cross(forward, up)  # NOQA
            rn = np.linalg.norm(right)
            if rn < 1e-6:
                fallback_axis = np.array([1.0, 0.0, 0.0], dtype=np.float32)
                if abs(float(np.dot(forward, fallback_axis))) > 0.9999:
                    fallback_axis = np.array([0.0, 0.0, 1.0], dtype=np.float32)

                right = np.cross(fallback_axis, forward)  # NOQA
                rn = np.linalg.norm(right)

            right = right / rn
            up = np.cross(right, forward)  # NOQA
            un = np.linalg.norm(up)
            up = up / (un if un >= 1e-6 else 1.0)
        else:
            right = np.cross(world_up, forward)  # NOQA

            rn = np.linalg.norm(right)
            if rn < 1e-6:
                right = np.array([1.0, 0.0, 0.0], dtype=np.float32)
                # rn = 1.0
            else:
                right = right / rn

            up = np.cross(forward, right)  # NOQA

            un = np.linalg.norm(up)
            if un < 1e-6:
                up = world_up.copy()
                # un = 1.0
            else:
                up = up / un

        # self._forward_norm = focal_distance
        # self._right_norm = rn
        # self._up_norm = un

        self._up = up
        self._right = right
        self._forward = forward

        self._focal_distance = focal_distance

    @_debug.logfunc
    def _update_views(self):
        """
        Update the views.
        """

        if not self._is_dirty:
            return

        self._calculate_camera()
        with self._context:
            self._is_dirty = False
            self._viewport = np.ascontiguousarray(GL.glGetIntegerv(GL.GL_VIEWPORT))

            self._projection = np.ascontiguousarray(np.array(
                GL.glGetDoublev(GL.GL_PROJECTION_MATRIX)).reshape((4, 4), order="F").T)

            self._modelview = np.ascontiguousarray(np.array(
                GL.glGetDoublev(GL.GL_MODELVIEW_MATRIX)).reshape((4, 4), order="F").T)

            self._clip = (self._projection @ self._modelview).astype(np.float32)

            # Cache the inverse once per dirty frame — avoids recomputing it
            # in every UnprojectPoint / get_position_on_focal_plane call.
            self._inv_clip = np.linalg.inv(self._clip)

            self._frustum_planes = self._extract_frustum_planes(self._clip)

            # Pre-extract normals and distances for faster AABB checking
            self._frustum_normals = np.ascontiguousarray(
                self._frustum_planes[:, 0:3])

            self._frustum_distances = np.ascontiguousarray(
                self._frustum_planes[:, 3])

            # Cache field of view — projection matrix only changes on resize.
            cot_half_fov = self._projection[1, 1]
            if cot_half_fov != 0.0:
                self._fov = math.degrees(math.atan(1.0 / cot_half_fov) * 2.0)
            else:
                self._fov = 0.0

    @_debug.logfunc
    def Rotate(self, dx: int, dy: int):
        """
        Moves the camera position keeping the focused point locked.

        :param dx: Mouse delta X coordinate.
        :type dx: int

        :param dy: Mouse delta Y coordinate.
        :type dy: int
        """

        if self.canvas.config.edit2d.enable:
            # Orbit is unreachable through the mouse/key dispatch layer
            # while locked (no rotate/pan_tilt binding class exists under
            # edit2d) — this guard is for direct callers.
            return

        self._is_dirty = True
        position = self._rotate_about(
            dx, dy, self._position, self._focal_position)

        self._position += position - self._position
        self._send_event(_events.EVT_GL_CAMERA_ORBIT)

    @staticmethod
    @_debug.logfunc
    def _rotate_about(dx: int, dy: int,
                      p1: _point.Point, p2: _point.Point) -> np.ndarray:
        """
        Moves the camera position keeping the focused point locked.
        """

        # This is a constant that can be adjusted. This is a hard limit
        # to prevent gimbal lock from occuring when looking straight up or
        # straight down. You can set it to a smaller number but do not increase
        # it past 89.9 otherwise gimbal lock can occur.
        p1 = p1.as_numpy
        p2 = p2.as_numpy

        max_pitch = 89.9

        offset = p1 - p2
        dist = np.linalg.norm(offset)

        if dist < 1e-6:
            return np.array([0.0, 0.0, 0.0], dtype=np.float32)

        up = _WORLD_UP.copy()

        def _rodrigues(v, k, angle_rad):
            """
            Execute the rodrigues operation.

            UNKNOWN details are inferred from the callable name and signature.

            :param v: Value for ``v``.
            :param k: Value for ``k``.
            :param angle_rad: Value for ``angle_rad``.
            :returns: Return value. UNKNOWN details.
            """

            k = k / np.linalg.norm(k)

            cos_a = math.cos(angle_rad)
            sin_a = math.sin(angle_rad)  # NOQA

            return ((v * cos_a) + (np.cross(k, v) * sin_a) +  # NOQA
                    (k * np.dot(k, v) * (1.0 - cos_a)))

        yaw_offset = _rodrigues(offset, up, math.radians(dx))
        yaw_offset_n = np.linalg.norm(yaw_offset)

        if yaw_offset_n < 1e-6:
            return np.array([0.0, 0.0, 0.0], dtype=np.float32)

        yaw_dir = yaw_offset / yaw_offset_n

        horiz_len = math.hypot(yaw_dir[0], yaw_dir[2])

        cur_pitch_deg = math.degrees(math.atan2(yaw_dir[1], horiz_len))

        desired_pitch = cur_pitch_deg + dy
        if desired_pitch > max_pitch or desired_pitch < -max_pitch:
            # block pitch movement entirely (yaw still applies)
            dy = 0.0

        if abs(dy) < 1e-6:
            final_offset = yaw_dir * dist
        else:
            right = np.cross(yaw_dir, up)  # NOQA
            rn = np.linalg.norm(right)

            if rn < 1e-6:
                final_offset = yaw_dir * dist
            else:
                right = right / rn
                rotated = _rodrigues(yaw_offset, right, math.radians(dy))

                rnorm = np.linalg.norm(rotated)

                if rnorm < 1e-6:
                    final_offset = yaw_dir * dist
                else:
                    # explicitly restore original distance to avoid shrink/grow
                    final_offset = rotated * (dist / rnorm)

        new_point = p2 + final_offset

        return _point.Point(new_point[0], new_point[1], new_point[2])

    @_debug.logfunc
    def PanTilt(self, dx: int, dy: int):
        """
        Pan and Tilt camera movements.

        Moves the camera center keeping the camera eye locked in place.

        :param dx: Mouse delta X coordinate.
        :type dx: int

        :param dy: Mouse delta Y coordinate.
        :type dy: int
        """

        if self.canvas.config.edit2d.enable:
            # Unreachable through normal dispatch while locked; guards the
            # direct call from _orient_to_mouse_on_focal_plane() (mouse
            # wheel cursor-reorientation), which bypasses _process_mouse.
            return

        self._is_dirty = True
        focal_point = self._rotate_about(
            dx, dy, self._focal_position, self._position)

        self._focal_position += focal_point - self._focal_position
        self._send_event(_events.EVT_GL_CAMERA_ROTATE)

    @_debug.logfunc
    def Zoom(self, delta, *_):
        """
        This has a similiar movement appearance as Dolly except there are hard
        limits as to how far it is able to move where as Dolly does not.
        This also doesn't change the camera position at all. It simply shinks
        or expands the distance between the focal point and the camera eye.

        :param delta: Amount to zoom.
        :type delta: float
        """

        min_distance = 0.1
        max_distance = 500.0

        offset = self._focal_position.as_numpy - self._position.as_numpy
        current_distance = np.linalg.norm(offset)
        if current_distance < 1e-6:
            return

        target_distance = current_distance - float(delta)

        if target_distance < min_distance:
            target_distance = min_distance
        elif target_distance > max_distance:
            target_distance = max_distance

        move_distance = current_distance - target_distance
        if abs(move_distance) < 1e-9:
            return

        move = self._forward * move_distance

        self._is_dirty = True
        self._position += move

        self._send_event(_events.EVT_GL_CAMERA_ZOOM)

    @_debug.logfunc
    def Walk(self, dx: int, dy: int, speed: float):
        """
        This movement is a bit tricky to explain in terms of camera movement.
        If you think about what you do as a person when you walk this will
        mimick as close as I could get to that movement. so basically if you
        want to walk straight forward and back you get that from this function.
        That is the same as a Dolly camera movement.

        if you go left and right the movement you get is as if you are turning
        on the ball of your foot. This is comparable to the "Pan" movement of a
        camera.

        If you hold left or right and press up or down at the same time you get
        a combination of the 2 above movements. kind of like what it would be
        like when you walk in an arc to either left or right.

        :param dx: Mouse delta X coordinate.
        :type dx: int

        :param dy: Mouse delta Y coordinate.
        :type dy: int

        :param speed: Speed to move.
        :type speed: float
        """

        # Build desired move from input
        # input_mag = math.sqrt((dx * dx) + (dy * dy))
        input_mag = math.hypot(dx, dy)

        if input_mag == 0:
            return

        move_dir = self._right * float(dx) + self._forward * float(dy)

        mdn = np.linalg.norm(move_dir)

        if mdn < 1e-6:
            return

        move_dir = move_dir / mdn
        move = move_dir * (input_mag * speed)

        self._is_dirty = True
        with self._focal_position and self._position:
            self._focal_position += move
            self._position += move

        self._send_event(_events.EVT_GL_CAMERA_WALK)

    @_debug.logfunc
    def TruckPedestal(self, dx: int, dy: int, speed: float):
        """
        Truck (left right) and Pedestal (up down).

        :param dx: Mouse delta X coordinate.
        :type dx: int

        :param dy: Mouse delta Y coordinate.
        :type dy: int

        :param speed: Speed to move.
        :type speed: float
        """

        # Build desired move from input
        # input_mag = math.sqrt((dx * dx) + (dy * dy))
        input_mag = math.hypot(dx, dy)

        if input_mag == 0:
            return

        move_dir = self._right * float(dx) + self._up * float(dy)

        mdn = np.linalg.norm(move_dir)
        if mdn < 1e-6:
            return

        move_dir = move_dir / mdn

        move = move_dir * (input_mag * speed)

        with self._position:
            self._position += move

        self._is_dirty = True
        self._focal_position += move

        self._send_event(_events.EVT_GL_CAMERA_TRUCKPEDISTAL)

    @_debug.logfunc
    def ProjectPoint(self, point: _point.Point | np.ndarray) -> _point.Point:
        """
        Projects a 3D world coordinate to a 2D screen coordinate.

        Uses the cached self._clip matrix (projection × modelview) rather
        than performing two separate matrix multiplications.

        :type point: _point.Point | np.ndarray
        :rtype: :class:`_point.Point`
        """

        if isinstance(point, _point.Point):
            world_point = np.array(
                [point.x, point.y, point.z, 1.0], dtype=np.float32)
        else:
            world_point = np.array(
                [point[0], point[1], point[2], 1.0], dtype=np.float32)

        # One matrix multiply instead of two (modelview then projection).
        clip_point = self._clip @ world_point

        if clip_point[3] == 0.0:
            return None

        ndc = clip_point[:3] / clip_point[3]

        screen_x = self._viewport[0] + (ndc[0] + 1.0) * 0.5 * self._viewport[2]
        screen_y = self._viewport[1] + (1.0 - ndc[1]) * 0.5 * self._viewport[3]

        # screen_x = self._viewport[0] + ((ndc[0] + 1.0) * 0.5 * self._viewport[2])
        # screen_y = self._viewport[1] + ((1.0 - ndc[1]) * 0.5 * self._viewport[3])
        screen_z = (ndc[2] + 1.0) * 0.5

        return _point.Point(screen_x, screen_y, screen_z)

    @_debug.logfunc
    def UnprojectPoint(self, point: _point.Point) -> _point.Point:
        """
        Projects a 2D screen coordinate to a 3D world coordinate.

        Uses the cached self._inv_clip matrix rather than recomputing the
        inverse on every call.

        :type point: :class:`_point.Point`
        :rtype: :class:`_point.Point`
        :raises ValueError: Raised when the operation cannot be completed.
        """

        ndc_x = (point.x - self._viewport[0]) / self._viewport[2] * 2.0 - 1.0
        ndc_y = (
            (self._viewport[3] - point.y - self._viewport[1]) /
            self._viewport[3] * 2.0 - 1.0)

        ndc_z = point.z * 2.0 - 1.0

        ndc_point = np.array([ndc_x, ndc_y, ndc_z, 1.0], dtype=np.float32)
        clip_point = self._inv_clip @ ndc_point

        if clip_point[3] == 0.0:
            raise ValueError("Perspective division failed (W=0 in clip space).")

        return _point.Point(*(clip_point[:3] / clip_point[3]))

    def unproject_from_ndc(self, x, y, z):
        """
        ndc: (x,y,z) in [-1,1]
        """

        clip = np.array([x, y, z, 1.0], dtype=np.float32)

        world = self.inv_clip.dot(clip)
        if np.isclose(world[3], 0.0):
            return None

        world /= world[3]
        return world[:3]

    def get_position_on_focal_plane(self, point: _point.Point) -> _point.Point:
        vx, vy, vw, vh = self.viewport

        ndc_x = (2.0 * (point.x - vx) / vw) - 1.0
        ndc_y = (2.0 * (vh - point.y - vy) / vh) - 1.0

        # One unproject — ray origin is the camera position itself
        far_world = self.unproject_from_ndc(ndc_x, ndc_y, 1.0)
        if far_world is None:
            return self.focal_position.copy()

        origin = self.position.as_numpy.astype(np.float32)
        direction = np.asarray(far_world, dtype=np.float32) - origin
        direction /= np.linalg.norm(direction)

        # camera.forward  == plane normal (already normalised)
        # camera.focal_distance == dot(focal_pos - origin, plane_normal)
        #                          because forward is the unit vec between them
        denom = np.dot(direction, self.forward)
        if abs(denom) < 1e-6:
            return self.focal_position.copy()

        t = self.focal_distance / denom
        if t < 0:
            return self.focal_position.copy()

        return _point.Point(*(origin + t * direction))

