    def Reset(self):
        with self._position and self._focal_position:
            self._focal_position.x = 0.0
            self._focal_position.y = self.canvas.config.floor.ground_height + 10.0
            self._focal_position.z = 0.0

            self._position.x = 0.0
            self._position.y = self.canvas.config.floor.ground_height + 100.0
            self._position.z = 75.0

        self._update_camera(None)

    def _update_camera(self, _=None):
        if self._position.y < self.canvas.config.floor.ground_height + 0.05:
            self._position.y = self.canvas.config.floor.ground_height + 0.05
            return

        if self.canvas.config.focal_target.enable and self._target is None:
            self._target = _focal_target.FocalPoint(self.canvas)
        elif not self.canvas.config.focal_target.enable and self._target is not None:
            self._target = None

        if self._context.is_locked:
            self._is_dirty = True
        else:
            wx.CallAfter(self.canvas.Refresh, False)

    @property
    def orthonormalized_axes(self):  # NOQA
        def normalize(v):
            v = np.array(v, dtype=float)
            n = np.linalg.norm(v)
            return v / (n if n != 0 else 1.0)

        # Returns forward, right, up (all unit) with forward pointing from eye -> center
        f = normalize((self._focal_position - self._position).as_numpy)
        r = normalize(np.cross(f, self._up))  # camera right  # NOQA
        u = np.cross(r, f)  # camera up re-orthonormalized  # NOQA
        return f, r, u

    @_debug.logfunc
    def GetObjectsInView(self, objs: list) -> list:
        if self._clip is None:
            self._is_dirty = True

        if self._is_dirty:
            self._update_views()

        aabb_in_frustum_planes = self._aabb_in_frustum_planes
        camera_pos = self._position.as_numpy
        normals = self._frustum_normals
        distances = self._frustum_distances
        
        # Separate focal points from regular objects early
        focal_points = []
        regular_objs = []
        for obj in objs:
            if isinstance(obj, _focal_target.FocalPoint):
                focal_points.append(obj)
            else:
                regular_objs.append(obj)
        
        # Filter regular objects using frustum culling
        visible_objs = []
        for obj in regular_objs:
            if aabb_in_frustum_planes(normals, distances, *obj.obj3d.aabb):
                # Calculate distance using numpy (avoid Line object creation)
                obj_pos = obj.obj3d.position.as_numpy
                diff = camera_pos - obj_pos
                dist_squared = np.dot(diff, diff)
                visible_objs.append((dist_squared, obj))
        
        # Add focal points with their distances
        for obj in focal_points:
            obj_pos = obj.obj3d.position.as_numpy
            diff = camera_pos - obj_pos
            dist_squared = np.dot(diff, diff)
            visible_objs.append((dist_squared, obj))
        
        # Sort by distance (far to near) - using tuple comparison is faster than lambda
        visible_objs.sort(reverse=True)
        
        # Separate opaque and transparent objects
        opaque = []
        transparent = []
        for _, obj in visible_objs:
            if obj.obj3d.is_opaque:
                opaque.append(obj)
            else:
                transparent.append(obj)
        
        # Return opaque first (far to near), then transparent (far to near)
        return opaque + transparent

    @staticmethod
    @_debug.logfunc
    def _aabb_in_frustum_planes(normals: np.ndarray, distances: np.ndarray, 
                                p1: _point.Point, p2: _point.Point) -> bool:
        """
        normals: (6,3) plane normals
        distances: (6,) plane distances
        p1, p2: AABB min and max points

        Returns True if intersects / inside, False if fully outside.
        """
        mn = p1.as_numpy
        mx = p2.as_numpy

        c = (mn + mx) * 0.5
        e = (mx - mn) * 0.5

        # signed distance from center to each plane
        s = (normals @ c) + distances  # (6,)

        # projected radius of extents onto plane normal
        r = (np.abs(normals) @ e)  # (6,)

        # if outside any plane -> reject
        return np.all((s + r) >= 0.0)

    @staticmethod
    @_debug.logfunc
    def _extract_frustum_planes(view_proj: np.ndarray) -> np.ndarray:
        """
        Returns planes as an array shape (6,4): [A,B,C,D] per plane.
        Planes are normalized (A,B,C length = 1).

        Order: left, right, bottom, top, near, far
        """
        m = np.asarray(view_proj, dtype=np.float64)

        # rows
        r0 = m[0, :]
        r1 = m[1, :]
        r2 = m[2, :]
        r3 = m[3, :]

        planes = np.stack([(r3 + r0),  # left
                          (r3 - r0),  # right
                          (r3 + r1),  # bottom
                          (r3 - r1),  # top
                          (r3 + r2),  # near  (OpenGL style)
                          (r3 - r2),  # far
                           ], axis=0)

        # normalize planes
        n = np.linalg.norm(planes[:, 0:3], axis=1)
        planes = planes / n[:, None]

        return planes

    def Set(self):
        self._calculate_camera()
        camera = self._position.as_float + self._focal_position.as_float + tuple(self._up.tolist())
        GLU.gluLookAt(*camera)
        self._update_views()

    @_debug.logfunc
    def _calculate_camera(self):
        pos = self._position.as_numpy
        fp = self._focal_position.as_numpy

        forward = fp - pos

        fn = np.linalg.norm(forward)
        if fn < 1e-6:
            return

        forward = forward / fn

        gf = np.linalg.norm(forward)
        if gf < 1e-6:
            forward_ground = np.array([0.0, 0.0, -1.0], dtype=np.float64)
        else:
            forward_ground = forward / gf

        self._forward_norm = gf

        world_up = np.array([0.0, 1.0, 0.0], dtype=np.float64)

        right = np.cross(world_up, forward_ground)  # NOQA

        rn = np.linalg.norm(right)
        if rn < 1e-6:
            right = np.array([1.0, 0.0, 0.0], dtype=np.float64)
        else:
            right = right / rn

        self._right_norm = rn

        up = np.cross(forward_ground, right)  # NOQA

        un = np.linalg.norm(up)
        if un < 1e-6:
            up = np.array([0.0, 1.0, 0.0], dtype=np.float64)
        else:
            up = up / un

        self._up_norm = un

        self._up = up
        self._right = right
        self._forward = forward_ground

        # Calculate focal distance directly without creating Line object
        diff = self._focal_position.as_numpy - self._position.as_numpy
        self._focal_distance = np.linalg.norm(diff)

    @_debug.logfunc
    def _update_views(self):
        if not self._is_dirty:
            return

        self._calculate_camera()
        with self._context:
            self._is_dirty = False
            self._viewport = np.ascontiguousarray(GL.glGetIntegerv(GL.GL_VIEWPORT))

            self._projection = np.ascontiguousarray(np.array(
                GL.glGetDoublev(GL.GL_PROJECTION_MATRIX)).reshape((4, 4), order="F").T)

            self._modelview = np.ascontiguousarray(np.array(
                GL.glGetDoublev(GL.GL_MODELVIEW_MATRIX)).reshape((4, 4), order="F").T)

            self._clip = (self._projection @ self._modelview).astype(np.float32)
            self._frustum_planes = self._extract_frustum_planes(self._clip)
            # Pre-extract normals and distances for faster AABB checking
            self._frustum_normals = self._frustum_planes[:, 0:3]
            self._frustum_distances = self._frustum_planes[:, 3]

    @_debug.logfunc
    def Rotate(self, dx, dy):
        """
        Moves the camera position keeping the focused point locked.
        """
        self._is_dirty = True
        position = self._rotate_about(dx, dy, self._position, self._focal_position)
        self._position += position - self._position

    @staticmethod
    @_debug.logfunc
    def _rotate_about(dx: int, dy: int,
                      p1: _point.Point, p2: _point.Point) -> np.ndarray:
        """
        Moves the camera position keeping the focused point locked.
        """
        # This is a constant that can be adjusted. This is a hard limit
        # to prevent gimbal lock from occuring when looking straight up or
        # straight down. You can set it to a smaller number but do not increase
        # it past 89.9 otherwise gimbal lock can occur.
        p1 = p1.as_numpy
        p2 = p2.as_numpy

        max_pitch = 89.9

        offset = p1 - p2
        dist = np.linalg.norm(offset)

        if dist < 1e-6:
            return np.array([0.0, 0.0, 0.0], dtype=np.float64)

        up = np.array([0.0, 1.0, 0.0], dtype=np.float64)

        def _rodrigues(v, k, angle_rad):
            k = k / np.linalg.norm(k)

            cos_a = math.cos(angle_rad)
            sin_a = math.sin(angle_rad)  # NOQA

            return ((v * cos_a) + (np.cross(k, v) * sin_a) +  # NOQA
                    (k * (np.dot(k, v)) * (1.0 - cos_a)))

        yaw_offset = _rodrigues(offset, up, math.radians(dx))
        yaw_offset_n = np.linalg.norm(yaw_offset)

        if yaw_offset_n < 1e-6:
            return np.array([0.0, 0.0, 0.0], dtype=np.float64)

        yaw_dir = yaw_offset / yaw_offset_n

        horiz_len = math.hypot(yaw_dir[0], yaw_dir[2])

        cur_pitch_deg = math.degrees(math.atan2(yaw_dir[1], horiz_len))

        desired_pitch = cur_pitch_deg + dy
        if desired_pitch > max_pitch or desired_pitch < -max_pitch:
            # block pitch movement entirely (yaw still applies)
            dy = 0.0

        if abs(dy) < 1e-6:
            final_offset = yaw_dir * dist
        else:
            right = np.cross(yaw_dir, up)  # NOQA
            rn = np.linalg.norm(right)

            if rn < 1e-6:
                final_offset = yaw_dir * dist
            else:
                right = right / rn
                rotated = _rodrigues(yaw_offset, right, math.radians(dy))

                rnorm = np.linalg.norm(rotated)

                if rnorm < 1e-6:
                    final_offset = yaw_dir * dist
                else:
                    # explicitly restore original distance to avoid shrink/grow
                    final_offset = rotated * (dist / rnorm)

        new_point = p2 + final_offset

        return _point.Point(new_point[0], new_point[1], new_point[2])

    @_debug.logfunc
    def PanTilt(self, dx, dy):
        """
        Moves the camera center keeping the camera eye locked in place.

        Pan and Tilt camera movements.
        """
        self._is_dirty = True
        focal_point = self._rotate_about(dx, dy, self._focal_position, self._position)
        self._focal_position += focal_point - self._focal_position

    @_debug.logfunc
    def Zoom(self, delta, *_):
        """
        This has a similiar movement appearance as Dolly except there are hard
        limits as to how far it is able to move where as Dolly does not.
        This also doesn't change the camera position at all. It simply shinks
        or expands the distance between the focal point and the camera eye.
        """
        move = self._forward * float(delta)

        # If moving would invert eye and pos, prevent crossing pos
        if delta > 0 and self._focal_distance <= 0.1:
            return

        elif delta < 0 and self._focal_distance >= 150.0:
            return

        self._is_dirty = True
        self._position += move

    @_debug.logfunc
    def Walk(self, dx, dy, speed):
        """
        This movement is a bit tricky to explain in terms of camera movement.
        If you think about what you do as a person when you walk this will
        mimick as close as I could get to that movement. so basically if you
        want to walk straight forward and back you get that from this function.
        That is the same as a Dolly camera movement.

        if you go left and right the movement you get is as if you are turning
        on the ball of your foot. This is comparable to the "Pan" movement of a
        camera.

        If you hold left or right and press up or down at the same time you get
        a combination of the 2 above movements. kind of like what it would be
        like when you walk in an arc to either left or right.
        """

        # Build desired move from input
        input_mag = math.sqrt((dx * dx) + (dy * dy))

        if input_mag == 0:
            return

        move_dir = self._right * float(dx) + self._forward * float(dy)

        mdn = np.linalg.norm(move_dir)

        if mdn < 1e-6:
            return

        move_dir = move_dir / mdn
        move = move_dir * (input_mag * speed)

        self._is_dirty = True
        with self._focal_position and self._position:
            self._focal_position += move
            self._position += move

    @_debug.logfunc
    def TruckPedestal(self, dx, dy, speed):
        """
        this is as the function name states. It is a Truck (left right)
        and a Pedestal (up down) movement
        """

        # Build desired move from input
        input_mag = math.sqrt((dx * dx) + (dy * dy))

        if input_mag == 0:
            return

        move_dir = self._right * float(dx) + self._up * float(dy)

        mdn = np.linalg.norm(move_dir)
        if mdn < 1e-6:
            return

        move_dir = move_dir / mdn

        move = move_dir * (input_mag * speed)

        with self._position:
            self._position += move

        self._is_dirty = True
        self._focal_position += move

    @_debug.logfunc
    def ProjectPoint(self, point: _point.Point) -> _point.Point:

        # Step 1: Convert to homogeneous world position (4D vector)
        # Add W=1 for homogeneous coords
        world_point = np.array([point.x, point.y, point.z, 1.0])

        # Step 2: Transform to view space using the modelview matrix
        view_point = np.dot(self._modelview, world_point)  # World space → View space

        # Step 3: Transform to clip space using the projection matrix
        clip_point = np.dot(self._projection, view_point)  # View space → Clip space

        # Perspective division: Normalize by the W component
        if clip_point[3] == 0.0:
            raise ValueError("Perspective division failed (W=0 in clip space).")

        # Normalize X, Y, Z by W in clip space
        ndc_point = clip_point[:3] / clip_point[3]

        # Step 4: Map from NDC [-1, 1] to screen space (viewport mapping)
        # Map X
        screen_x = (self._viewport[0] +
                    ((ndc_point[0] + 1.0) *
                     0.5 * self._viewport[2]))

        # Map Y
        screen_y = (self._viewport[1] +
                    ((1.0 - ndc_point[1]) *
                     0.5 * self._viewport[3]))

        # Map Z from [-1, 1] to [0, 1]
        screen_z = (ndc_point[2] + 1.0) * 0.5

        # Step 5: Return the screen-space coordinates
        return _point.Point(screen_x, screen_y, screen_z)

    @_debug.logfunc
    def UnprojectPoint(self, point: _point.Point) -> _point.Point:
        # Step 1: Map screen space [x, y, z] to normalized device coordinates (NDC)
        ndc_x = ((point.x - self._viewport[0]) /
                 self._viewport[2] * 2.0 - 1.0)

        # ndc_y = ((_decimal(point.y) - _decimal(self._viewport[1])) /
        #          _decimal(self._viewport[3]) * _decimal(2.0) - _decimal(1.0))
        ndc_y = ((self._viewport[3] - point.y -
                  self._viewport[1]) / self._viewport[3] *
                 2.0 - 1.0)

        # Depth is mapped [0, 1] → [-1, 1]
        ndc_z = point.z * 2.0 - 1.0

        # Homogeneous NDC
        ndc_point = np.array([ndc_x, ndc_y, ndc_z, 1.0])

        # Step 2: Compute the inverse of the projection * modelview matrix
        # Combine projection and modelview
        clip_matrix = np.dot(self._projection, self._modelview)

        # Invert the combined matrix
        inverse_clip_matrix = np.linalg.inv(clip_matrix)

        # Step 3: Transform from NDC to clip space
        clip_point = np.dot(inverse_clip_matrix, ndc_point)

        # Step 4: Perspective division to get world-space coordinates
        if clip_point[3] == 0.0:
            raise ValueError("Perspective division failed (W=0 in clip space).")

        # Normalize by W in homogeneous space
        world_coords = clip_point[:3] / clip_point[3]

        # Return unprojected world-space coordinates
        return _point.Point(*world_coords)
