def convert_dimension(dim):

    if not dim:
        return 0.0

    if dim == 'N/Amm':
        return 0.0

    if ',' in dim and '.' not in dim:
        dim = dim.replace(',', '.')

    if ',' in dim:
        dim = [item.strip() for item in dim.split(',')]
        res = set()

        for items in dim:
            items = convert_dimension(items)
            if isinstance(items, list):
                for item in items:
                    res.add(item)
            else:
                res.add(items)

        return sorted(list(res))

    if '<br/>' in dim:
        dim = dim.split('<br/>')
        dim = set([convert_dimension(item) for item in dim])
        return sorted(list(dim))

    if '/' in dim:
        dim = dim.split('/')
        res = set()
        for item in dim:
            item = convert_dimension(item)
            if isinstance(item, list):
                for itm in item:
                    res.add(itm)
            else:
                res.add(item)
        return sorted(list(res))

    dim = dim.replace('mm', '')
    if '.' not in dim:
        dim += '.0'

    return float(dim)


