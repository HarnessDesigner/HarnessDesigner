def convert_wire_cross(value):
    if not value:
        return 0.0, 0.0

    value = value.replace('...', ' - ')
    value = value.replace('…', ' - ')
    value = value.replace('(FLR)', '')
    value = value.replace('mm²', '')
    value = value.replace('mm', '').strip()

    if '-' not in value:
        value += ' - ' + value

    min_val, max_val = [item.strip() for item in value.split('-')]
    if '.' not in min_val:
        min_val += '.0'

    if '.' not in max_val:
        max_val += '.0'

    return float(min_val), float(max_val)


def convert_amps(value):
    if not value:
        return 0

    new_value = ''

    for char in value:
        if char != '.' and not char.isdigit():
            continue

        new_value += char

    if new_value.startswith('.'):
        new_value = new_value[1:]

    if 'mA' not in value:
        if '.' not in new_value:
            new_value += '.0'

        new_value = int(float(new_value) * 1000)
    else:
        new_value = int(new_value)

    return new_value


